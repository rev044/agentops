// ----- review -----

var constraintReviewCmd = &cobra.Command{
	Use:   "review",
	Short: "List constraints compiled >90 days ago without recent citation",
	RunE: func(cmd *cobra.Command, args []string) error {
		idx, err := loadConstraintIndex()
		if err != nil {
			return err
		}

		cutoff := time.Now().AddDate(0, 0, -90)
		stale := []constraintEntry{}

		for _, c := range idx.Constraints {
			if c.Status == "retired" {
				continue
			}
			compiled, parseErr := time.Parse(time.RFC3339, c.CompiledAt)
			if parseErr != nil {
				// Try date-only format
				compiled, parseErr = time.Parse("2006-01-02", c.CompiledAt)
				if parseErr != nil {
					continue
				}
			}
			if compiled.Before(cutoff) {
				stale = append(stale, c)
			}
		}

		if GetOutput() == "json" {
			enc := json.NewEncoder(os.Stdout)
			enc.SetIndent("", "  ")
			return enc.Encode(stale)
		}

		if len(stale) == 0 {
			fmt.Println("No constraints need review.")
			return nil
		}

		fmt.Printf("%-30s %-10s %-20s %s\n", "ID", "STATUS", "COMPILED", "TITLE")
		fmt.Printf("%-30s %-10s %-20s %s\n", "--", "------", "--------", "-----")
		for _, c := range stale {
			id := c.ID
			if len(id) > 30 {
				id = id[:27] + "..."
			}
			compiled := c.CompiledAt
			if len(compiled) > 20 {
				compiled = compiled[:20]
			}
			title := c.Title
			if len(title) > 50 {
				title = title[:47] + "..."
			}
			fmt.Printf("%-30s %-10s %-20s %s\n", id, c.Status, compiled, title)
		}
		fmt.Printf("\n%d constraint(s) need review (>90 days old)\n", len(stale))
		return nil
	},
}

